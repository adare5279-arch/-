function normalizeName(name: string): string {
  // 공백 제거 후 비교 (직책 등은 호출 측에서 제거)
  return name.replace(/\s+/g, '');
}

// 해당 위원회의 의원 목록 페이지 URL
export function ggcCommitteeUrl(committee: string): string | null {
  const code = GGC_COMMITTEE_CODE[committee];
  if (!code) return null;
  return `${GGC_BASE}/site/main/memberInfo/actvMmbr/list?menu=committee&miCommitteeCode=${code}`;
}

// 개별 의원 홈페이지 URL (없으면 null)
export function ggcMemberUrl(committee: string, name: string): string | null {
  const key = normalizeName(name);
  const id =
    GGC_MEMBER_ID[committee]?.[key] ??
    // 같은 위원회에서 못 찾으면 다른 위원회에서 유일하게 매칭되는 경우 사용
    uniqueGlobalId(key);
  if (!id) return null;
  return `${GGC_BASE}/site/lwmkr/blog/${id}/${GGC_ERA}`;
}

// 전체 위원회에서 동일 이름이 단 하나의 ID로만 매칭될 때만 반환(동명이인 방지)
function uniqueGlobalId(name: string): number | null {
  const ids = new Set<number>();
  for (const committee of Object.keys(GGC_MEMBER_ID)) {
    const id = GGC_MEMBER_ID[committee][name];
    if (id) ids.add(id);
  }
  return ids.size === 1 ? [...ids][0] : null;
}

// 의원 클릭 시 이동할 최종 URL: 개별 홈페이지 우선, 없으면 위원회 목록 페이지
export function ggcLinkFor(committee: string, name: string): string | null {
  return ggcMemberUrl(committee, name) ?? ggcCommitteeUrl(committee);
}
